
// Autoboxing

public class Testone {
    public static void main(String[] args) {
        // Autoboxing
        int a = 100;
        // Primitive DT to object
        // Integer b = Integer.valueOf(a); 
        
        
        // automatically converting primitive to object
        Integer b = a; 
        System.out.println(a);
        System.out.println(b);
    }    
}
